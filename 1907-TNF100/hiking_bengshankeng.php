              <p>The North Face推出”黃金越野跑計畫” ，將依循無痕山林作為最高指導原則，秉持『尊重自然』和『永續與共享』的理念，通過戶外運動形式，實現與自然的共存，並培養一種熱愛和尊重自然的精神。透過無痕山林、手作步道、環境教育，讓越野跑者、登山者、當地居民及遊客，相互尊重，鼓勵大家一起愛護自然森林，共同為臺灣環境保護而努力，同時，分享對山林的熱愛，分享包容的價值。</p>
