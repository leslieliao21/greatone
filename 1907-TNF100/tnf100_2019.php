            <p>The North Face® 成立於1966 年，是美國上市公司 威富集團的重要一員，總部位於美國加州，致力於為戶外運動員的每一次嚴酷探險提供專業裝備。" The North Face® "
              這個名稱源於山上最冷、最難攀爬的北坡。形象來源是Half Dome，這座來自美國加州Yosemite National Park（優勝美地國家公園）裡的山峰，因此中文名為 " 北面
              "，意味著探索最難最險的戶外精神。1997 年，The North Face® 採用全新的宣傳標語—" 探索永不停止 "，從此成為品牌最重要的精神口號。作為當今全球戶外運動領導品牌，The North
              Face®以經過運動員測試探索、適應各類戶外需求的產品說明世人探索、挑戰人類潛能的極限；與此同時，品牌始終致力於保護戶外環境，通過各類可持續發展專案將人類對自然環境所產生的影響降到最低。The North
